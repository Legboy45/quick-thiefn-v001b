game.onUpdate(function () {
    if (End_triggered) {
        if (RunningDelay == 40) {
            FootstepDelay = 200
            animforRunninginEnd()
        } else {
            FootstepDelay = 350
            animforEnd()
        }
    } else {
        if (RunningDelay == 40) {
            FootstepDelay = 200
            animforRunning()
        } else {
            FootstepDelay = 400
            anim()
        }
    }
    if (In_Level) {
        if (controller.A.isPressed()) {
            RunningTrail("running")
            if (controller.left.isPressed()) {
                if (RunningDelay < 40) {
                    RunningDelay += 1
                } else if (RunningDelay == 40) {
                    isRunning = true
                    controller.moveSprite(mySprite, 180, 0)
                } else {
                    isRunning = false
                    controller.moveSprite(mySprite, 100, 0)
                }
            } else if (controller.right.isPressed()) {
                if (RunningDelay < 40) {
                    RunningDelay += 1
                } else if (RunningDelay == 40) {
                    isRunning = true
                    controller.moveSprite(mySprite, 180, 0)
                } else {
                    isRunning = false
                    controller.moveSprite(mySprite, 100, 0)
                }
            } else {
                isRunning = false
                RunningDelay = 0
            }
        } else {
            isRunning = false
            RunningDelay = 0
        }
    }
})

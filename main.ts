function CoinCollectibleTiles (coinimagelist: any[], collectibleimagelist: any[]) {
    CoinList = [
    assets.image`points_blue`,
    assets.image`points_red`,
    assets.image`points_green`,
    assets.image`points_orange`,
    assets.image`points_purple`
    ]
    CollectibleList = [
    assets.image`Collectible_tree`,
    assets.image`Collectible_gear`,
    assets.image`Collectible_skull`,
    assets.image`Collectible_water`,
    assets.image`Collectible_cd`
    ]
    for (let value of tiles.getTilesByType(assets.tile`myTile17`)) {
        CoinSprite = sprites.create(CoinList._pickRandom(), SpriteKind.Coin)
        tiles.placeOnTile(CoinSprite, value)
        tiles.setTileAt(value, assets.tile`transparency16`)
    }
    for (let value of tiles.getTilesByType(assets.tile`myTile18`)) {
        item = CollectibleList._pickRandom()
        CollectibleSprite = sprites.create(item, SpriteKind.Collectible)
        CollectibleList.removeAt(CollectibleList.indexOf(item))
        tiles.placeOnTile(CollectibleSprite, value)
        tiles.setTileAt(value, assets.tile`transparency16`)
    }
}
